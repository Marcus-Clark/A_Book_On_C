int main(void)
{
   int   c;

   printf("Clock ticks: %ld\n", clock());   /* start the clock */
   while ((c = getchar()) != EOF)
      if (islower(c))
         putchar(toupper(c));
      else if (isupper(c))
         putchar(tolower(c));
      else if (isdigit(c))
         putchar('x');
      else if (ispunct(c))
         ;
      else if (isspace(c)) {
         putchar(c);
         putchar(c);
      }
   printf("Clock ticks: %ld\n", clock());   /* ticks up to now */
   return 0;
}
