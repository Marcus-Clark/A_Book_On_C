#include <iostream.h>

int main()
{
   char   *p[2][3] = {{"abc", "defg", "hi"},
                      {"jklmno", "pqrstuvw", "xyz"}};

   cout << "\n"
           "***p                 = " << ***p                 << "\n"
           "**p[1]               = " << **p[1]               << "\n"
           "**(p[1] + 2)         = " << **(p[1] + 2)         << "\n"
           "*(*(p + 1) + 1)[7]   = " << *(*(p + 1) + 1)[7]   << "\n" 
           "(*(*(p + 1) + 1))[7] = " << (*(*(p + 1) + 1))[7] << "\n" 
           "*(p[1][2] + 2)       = " << *(p[1][2] + 2)       << "\n"
           "\n";
}
